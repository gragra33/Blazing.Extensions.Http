namespace Blazing.Extensions.Http.Models;

/// <summary>
/// Represents the available bit units for transfer rate calculations.
/// </summary>
public enum BitUnit
{
    /// <summary>
    /// Bits per second.
    /// </summary>
    b,
    
    /// <summary>
    /// Kilobits per second.
    /// </summary>
    Kb,
    
    /// <summary>
    /// Megabits per second.
    /// </summary>
    Mb,
    
    /// <summary>
    /// Gigabits per second.
    /// </summary>
    Gb,
    
    /// <summary>
    /// Terabits per second.
    /// </summary>
    Tb
}